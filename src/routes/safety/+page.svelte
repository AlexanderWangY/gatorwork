	<div class="mb-6 flex flex-col gap-2">
		<h1 class="text-brand-black text-4xl font-bold">Safety Tips for GatorMarket</h1>
		<p class="text-brand-gray text-sm">Last Updated: April 6th, 2025</p>
	</div>

	<p class="text-brand-gray mb-10 text-lg">
		GatorMarket is a marketplace built exclusively for UF students — only <strong>@ufl.edu</strong> emails
		can sign up. This adds a layer of trust, but safety still matters when meeting up and exchanging
		items. Here are essential tips to help you shop smart and stay safe, especially if you’re new to
		second-hand deals.
	</p>

	<div class="space-y-10">
		<section>
			<h2 class="text-brand-blue text-2xl font-semibold">1. Meet in Safe, Public Places</h2>
			<p class="text-brand-gray">
				Always meet on campus or in well-lit public areas. Good spots include:
			</p>
			<ul class="text-brand-gray my-2 mt-2 ml-6 list-disc">
				<li>Library West or Marston lobby</li>
				<li>Reitz Union common areas</li>
				<li>Any busy coffee shop on or near campus</li>
			</ul>
			<p class="text-brand-gray">Avoid dorm rooms or apartments for first-time meetups.</p>
		</section>

		<section>
			<h2 class="text-brand-blue mb-2 text-2xl font-semibold">2. Bring a Friend if You Can</h2>
			<p class="text-brand-gray">
				If you’re nervous or it’s your first time doing a trade, bring a roommate or friend with
				you. It makes the meetup smoother and safer.
			</p>
		</section>

		<section>
			<h2 class="text-brand-blue mb-2 text-2xl font-semibold">3. Inspect Before You Pay</h2>
			<p class="text-brand-gray">
				Check the item carefully before handing over any money. If it’s clothing, look for rips,
				stains, or missing parts. If it's electronics, make sure it powers on and works as expected.
			</p>
		</section>

		<section>
			<h2 class="text-brand-blue mb-2 text-2xl font-semibold">4. Use Exact Cash or Trusted Apps</h2>
			<p class="text-brand-gray">
				For in-person trades, bring exact cash to avoid change issues. If using a digital app (like
				Venmo), send it only after inspecting the item. Never send payment in advance to hold an
				item unless you know the person.
			</p>
		</section>

		<section>
			<h2 class="text-brand-blue mb-2 text-2xl font-semibold">5. Don’t Share Personal Info</h2>
			<p class="text-brand-gray">
				There’s no need to give out your phone number, dorm address, or Instagram. Chat through
				GatorMarket until you're ready to meet.
			</p>
		</section>

		<section>
			<h2 class="text-brand-blue mb-2 text-2xl font-semibold">6. Trust Your Gut</h2>
			<p class="text-brand-gray">
				If something feels off — the price is too good, the other person is acting pushy, or they
				want to meet somewhere sketchy — walk away. It’s okay to cancel or change your mind.
			</p>
		</section>

		<section>
			<h2 class="text-brand-blue mb-2 text-2xl font-semibold">7. Report Suspicious Behavior</h2>
			<p class="text-brand-gray">
				GatorMarket is only for real UF students. If you see fake listings, harassment, or anyone
				violating the rules, report it to support or contact our team directly. Keeping the platform
				safe is a team effort.
			</p>
		</section>
